# Destination Surcharge
def dest(param):
    dest_surcharge = DEST_SURCHARGES.get(param["de_to_address_suburb"])

    if (
        dest_surcharge
        and dest_surcharge["postalCode"] == param["de_to_address_postcode"]
    ):
        return {
            "name": "Destination Surcharge",
            "description": "Surcharge for specific destination.",
            "value": dest_surcharge["amount"],
        }
    else:
        return None


